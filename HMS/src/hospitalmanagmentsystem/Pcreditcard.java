     private void displayTable(){
         
         int count;
     
          try{
            Class.forName("com.mysql.jdbc.Driver");
	
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/hms","root","");
            
            String sql2 = "select Bill_No,User_Id,Payers_Name,Bill_Type,Payment_Method,Date,Time,Amount from payment order by Bill_No DESC LIMIT 1";
            PreparedStatement result1 = conn.prepareStatement(sql2);
            ResultSet retrieve1 = result1.executeQuery();
            ResultSetMetaData r = retrieve1.getMetaData();
            
            count = r.getColumnCount();
            
              DefaultTableModel df = (DefaultTableModel) creditDetails.getModel();
              df.setRowCount(0);
              
              while(retrieve1.next()){
                  
                  Vector v = new Vector();
                  
                  for(int a=1; a<=count;a++){
                      
                      v.add(retrieve1.getString("Bill_No"));
                      v.add(retrieve1.getString("User_Id"));
                      v.add(retrieve1.getString("Payers_Name"));
                      v.add(retrieve1.getString("Bill_Type"));
                      v.add(retrieve1.getString("Payment_Method"));
                      v.add(retrieve1.getString("Date"));
                      v.add(retrieve1.getString("Time"));
                      v.add(retrieve1.getString("Amount"));
                  
                  }
                  
                  df.addRow(v);
              }
                     
        }catch(Exception ex){
         
      }
        
    }  
     
     
     
    

