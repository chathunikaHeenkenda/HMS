    private void cardNumberKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cardNumberKeyReleased
        // TODO add your handling code here:
        
        String creditNum = cardNumber.getText();
        
        
        int[] ints = new int[creditNum.length()];
        
		for (int i = 0; i < creditNum.length(); i++) 
                {
			ints[i] = Integer.parseInt(creditNum.substring(i, i + 1));
		}
                
		for (int i = ints.length - 2; i >= 0; i = i - 2) 
                {
			int j = ints[i];
			j = j * 2;
                        
			if (j > 9) 
                        {
				j = j % 10 + 1;
			}
                        
			ints[i] = j;
		}
                
		int sum = 0;
                
		for (int i = 0; i < ints.length; i++)
                {
			sum += ints[i];
		}
                
		if (sum % 10 == 0 && creditNum.length()==16) 
                {
			creditMsg.setText("Valid Credit Card");
		} 
                else 
                {
			creditMsg.setText("Invalid Credit Card");
		}
    }//GEN-LAST:event_cardNumberKeyReleased

