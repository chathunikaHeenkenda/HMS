    private void admissionTable(){
        
      
       Calendar cal = Calendar.getInstance();
       SimpleDateFormat sdf = new SimpleDateFormat("HH:mm");
       time.setText( sdf.format(cal.getTime()));
       cDate.setText(java.time.LocalDate.now().toString());
       
         int c;

        try {

            Class.forName("com.mysql.jdbc.Driver");

            con1 = DriverManager.getConnection("jdbc:mysql://localhost/hms", "root", "");

            insert = con1.prepareStatement("select * from room,patient,ward where room.User_Id = patient.User_Id and ward.Wd_No = room.Wd_No and In_Date is not null and Out_Date is null order by In_date desc");
            ResultSet rs = insert.executeQuery();
            ResultSetMetaData res = rs.getMetaData();

            c = res.getColumnCount();

            DefaultTableModel df = (DefaultTableModel) jTable1.getModel();
            df.setRowCount(0);

            while (rs.next()) {

                Vector v2 = new Vector();

                for (int a = 1; a <= c; a++) {

                    v2.add(rs.getString("room.User_Id"));
                    v2.add(rs.getString("FullName"));;
                    v2.add(rs.getString("In_Date"));
                    v2.add(rs.getString("Room_No"));
                    v2.add(rs.getString("W_Name"));
                    v2.add(rs.getString("Phone"));
                }

                df.addRow(v2);
            }
             } catch (SQLException ex) {

            Logger.getLogger(HIpd.class.getName()).log(Level.SEVERE, null, ex);

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(HIpd.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
