    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        this.pName.setText("");
            phoneNum.setText("");
            age.setText("");
        try {

            Class.forName("com.mysql.jdbc.Driver");

            con1 = DriverManager.getConnection("jdbc:mysql://localhost/hms", "root", "");
            
            insert = con1.prepareStatement("select 1 from room,patient where room.User_Id = patient.User_Id and patient.User_Id = ? and Out_Date is null");
            insert.setString(1, pId.getText());
            ResultSet rs2 = insert.executeQuery();
            
            int val = 0;
            
            while(rs2.next()){
                
                val = rs2.getInt("1");
            }
            
            if(val == 1){
                JOptionPane.showMessageDialog(this, "This patient is currently in admission");
                phoneNum.setText("");
                pName.setText("");
                age.setText("");
            }
            
            else{
            
                insert = con1.prepareStatement("select FullName,Age,Phone from patient where User_Id = ?");
                insert.setString(1, pId.getText());
                ResultSet rs = insert.executeQuery();


                while (rs.next()) {

                    pName.setText(rs.getString("FullName"));
                    age.setText(rs.getString("Age"));
                    phoneNum.setText(rs.getString("Phone"));
                }

                 if(pName.getText().equals(""))
                       
                     JOptionPane.showMessageDialog(this, "Invalid patient id");

                }
             } catch (SQLException ex) {

            Logger.getLogger(HIpd.class.getName()).log(Level.SEVERE, null, ex);

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(HIpd.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

