    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        
        try{
            Class.forName("com.mysql.jdbc.Driver");
	
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/hms","root","");
            
          
            
            String sql1="select  Bill_No +1 as Bill_Num from payment group by Bill_No order by Bill_No desc limit 1";
            
            PreparedStatement retrieve1 = conn.prepareStatement(sql1);
            ResultSet result1 = retrieve1.executeQuery();
      
                 if(result1.next())
                { 
                    billNo.setText(result1.getString("Bill_Num"));  
                }
                
              String sql2="select ph.User_Id ,ph.Price from pharmacybill ph,patient p where ph.User_Id=p.User_Id and p.phone = ?";
            
            PreparedStatement retrieve2 = conn.prepareStatement(sql2);
            retrieve2.setInt(1,Integer.parseInt(phone.getText()));
            ResultSet result2 = retrieve2.executeQuery();
      
                 if(result2.next())
                { 
                    totalAmount.setText(result2.getString("Price"));  
                    patientId.setText(result2.getString("User_Id")); 
                }   
            
             }catch(Exception ex){

            JOptionPane.showMessageDialog(this,"Record Not Found");
	
        }
    }//GEN-LAST:event_jButton4ActionPerformed

