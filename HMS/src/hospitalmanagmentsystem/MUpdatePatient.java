    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        try {
            // TODO add your handling code here:
            Class.forName("com.mysql.jdbc.Driver");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(MUpdatePatient.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/hms","root", "");
            Statement st = con.createStatement();
            int User_id = Integer.parseInt(userID.getText());
            String sql = "select * from patient where User_id = '"+User_id+"'";
            ResultSet rs = st.executeQuery(sql);
            
            if(rs.next()){
                    userID.setText(rs.getString("User_id"));
                    jComboBox3.setSelectedItem(rs.getString("Title"));
                    name3.setText(rs.getString("FullName"));
                    jDateChooser7.setDate(rs.getDate("Dob"));
                    jTextField4.setText(rs.getString("Age"));
                    if("Male".equals(rs.getString("Gender"))){
                        jRadioButton11.setSelected(true);
                    }
                    else if ("Female".equals(rs.getString("Gender"))){
                        jRadioButton9.setSelected(true);
                    }
                    else{
                        jRadioButton11.setSelected(false);
                        jRadioButton9.setSelected(false);
                    }
                    
                    phone2.setText(rs.getString("Phone"));
                    email2.setText(rs.getString("Email"));
                    NIC2.setText(rs.getString("NIC"));
                    jDateChooser8.setDate(rs.getDate("Date"));
                    if("single".equals(rs.getString("Status"))){
                        jRadioButton10.setSelected(true);
                    }
                    else if ("Married".equals(rs.getString("Status"))){
                        jRadioButton12.setSelected(true);
                    }
                    
            
            }
            else{
                JOptionPane.showMessageDialog(null,"Details not found");
            
            
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(MUpdatePatient.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton13ActionPerformed

