    private void jTextField4MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTextField4MouseReleased
        // TODO add your handling code here:
        String dateOfBirth = ((JTextField)jDateChooser7.getDateEditor().getUiComponent()).getText();
        //nsplit dateofbirth variable to string array
        String dob[] = dateOfBirth.split("/");
        //convert String to int
        
        int day = Integer.parseInt(dob[0]);
        int month = Integer.parseInt(dob[1]);
        int year = Integer.parseInt(dob[2]);
        
        LocalDate selecteddate = LocalDate.of(year, month, day);
        LocalDate currentDate = LocalDate.now();
        
        int resultYear = Period.between(selecteddate,currentDate).getYears();
        int resultMonth = Period.between(selecteddate,currentDate).getMonths();
        int resultDays = Period.between(selecteddate,currentDate).getDays();
        
        jTextField4.setText(""+resultYear);
    }//GEN-LAST:event_jTextField4MouseReleased

